SELECT LAST_NAME FROM PROFILE WHERE ROLL = (SELECT ROLL_NO FROM STUDENT WHERE CGPA > 3.0);
SELECT LAST_NAME, CONTACT_NO, EMAIL FROM PROFILE WHERE ROLL IN (SELECT ROLL_NO FROM PROFESSION WHERE COMPANY = 'Samsung');
SELECT FIRST_NAME, CONTACT_NO FROM PROFILE WHERE ROLL IN (SELECT ROLL_NO FROM DEGREE WHERE COUNTRY = 'England' AND ROLL_NO = (SELECT ROLL_NO FROM PROFESSION WHERE TYPE_OF_JOB = 'Parttime'));
