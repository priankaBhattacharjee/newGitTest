--JOIN

SELECT PROFILE.FIRST_NAME, PROFESSION.COMPANY, PROFESSION.JOB_TITLE, PROFESSION.SALARY FROM PROFILE INNER JOIN PROFESSION on PROFILE.ROLL = PROFESSION.ROLL_NO AND PROFILE.FIRST_NAME = 'Prianka';
SELECT ROLL_NO, student.CGPA, student.BACKLOG_COURSES_NO, student.SPECIALITY, profession.COMPANY, profession.TYPE_OF_JOB, profession.SALARY FROM student NATURAL JOIN profession;
SELECT PROFILE.LAST_NAME, PROFESSION.JOB_TITLE, PROFESSION.COMPANY FROM PROFILE LEFT OUTER JOIN PROFESSION ON PROFILE.ROLL = PROFESSION.ROLL_NO;
SELECT PROFILE.LAST_NAME FROM PROFILE LEFT OUTER JOIN PROFESSION ON PROFESSION.JOB_TITLE = NULL;
SELECT DEGREE.UNIVERSITY, PROFILE.LAST_NAME, PROFILE.CONTACT_NO, PROFILE.EMAIL FROM PROFILE INNER JOIN DEGREE ON PROFILE.ROLL = DEGREE.ROLL_NO;


