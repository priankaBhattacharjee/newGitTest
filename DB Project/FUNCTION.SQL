CREATE OR REPLACE PROCEDURE AVG_CG IS
AVERAGE STUDENT.CGPA%TYPE;
BEGIN
	SELECT AVG(CGPA) INTO AVERAGE FROM STUDENT;
	DBMS_OUTPUT.PUT_LINE('AVERAGE CGPA OF KUETIANS: ' || AVERAGE);
END;
/
